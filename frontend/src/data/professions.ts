export function termLabel(t: Term, lang: Language): string {
  return lang === 'en' ? t.en : t.zh;
}

export function filterTerms(list: Term[], q: string): Term[] {
  const s = q.trim().toLowerCase();
  if (!s) return list;
  return list.filter((t) => t.zh.toLowerCase().includes(s) || t.en.toLowerCase().includes(s));
}
