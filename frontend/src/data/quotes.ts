export interface BookQuote {
  text: string;
  source: string;  // 书名
}

/** 从一组书里收集所有可用书摘，返回 [{ text, source }] */
export function collectQuotes(books: Array<{ id: string; title: string }>): BookQuote[] {
  const out: BookQuote[] = [];
  for (const b of books) {
    const qs = FAMOUS_QUOTES[b.id];
    if (qs && qs.length > 0) {
      for (const q of qs) out.push({ text: q, source: b.title });
    }
  }
  return out;
}

/** 随机抽一句；没有可用书摘时返回 null */
export function randomQuote(books: Array<{ id: string; title: string }>): BookQuote | null {
  const list = collectQuotes(books);
  if (list.length === 0) return null;
  return list[Math.floor(Math.random() * list.length)];
}
